@dataclass
class AutoResearchProfile:
    """
    Autonomous research configuration profile.

    Generated by AutonomousAnalyzer based on query content analysis.
    Contains all settings needed to execute an optimized research task.
    """
    # ToT Configuration
    use_tot: bool = False
    tot_mode: str = "standard"  # "standard" | "hybrid" | "full"

    # Tool Selection
    tools: Set[str] = field(default_factory=set)

    # Source Selection
    sources: Set[str] = field(default_factory=set)  # "surface" | "archive" | "academic" | "dark" | "leaked"

    # Privacy Configuration
    privacy_level: str = "STANDARD"  # "STANDARD" | "HIGH" | "MAXIMUM"
    use_tor: bool = False

    # Model Selection
    models_needed: Set[str] = field(default_factory=set)  # "hermes" | "modernbert" | "gliner"

    # Execution Parameters
    depth: str = "STANDARD"  # "QUICK" | "STANDARD" | "DEEP" | "EXHAUSTIVE"
    max_time: float = 300.0  # Estimated execution time in seconds

    # Explanation
    reasoning: str = ""

    def to_dict(self) -> Dict[str, Any]:
        """Convert profile to dictionary for serialization."""
        return {
            "use_tot": self.use_tot,
            "tot_mode": self.tot_mode,
            "tools": list(self.tools),
            "sources": list(self.sources),
            "privacy_level": self.privacy_level,
            "use_tor": self.use_tor,
            "models_needed": list(self.models_needed),
            "depth": self.depth,
            "max_time": self.max_time,
            "reasoning": self.reasoning,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "AutoResearchProfile":
        """Create profile from dictionary."""
        return cls(
            use_tot=data.get("use_tot", False),
            tot_mode=data.get("tot_mode", "standard"),
            tools=set(data.get("tools", [])),
            sources=set(data.get("sources", [])),
            privacy_level=data.get("privacy_level", "STANDARD"),
            use_tor=data.get("use_tor", False),
            models_needed=set(data.get("models_needed", [])),
            depth=data.get("depth", "STANDARD"),
            max_time=data.get("max_time", 300.0),
            reasoning=data.get("reasoning", ""),
        )


