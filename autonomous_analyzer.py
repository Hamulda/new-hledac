    def _detect_tools(self, query: str) -> Set[str]:
        """
        Detect which intelligence tools should be activated.

        Args:
            query: Research query

        Returns:
            Set of tool names to activate
        """
        detected_tools: Set[str] = set()
        query_lower = query.lower()

        for tool, patterns in self._compiled_tool_patterns.items():
            for pattern in patterns:
                if pattern.search(query_lower):
                    detected_tools.add(tool)
                    break  # Found match for this tool, move to next

        # Always include web_intelligence as fallback for general queries
        if not detected_tools:
            detected_tools.add("web_intelligence")

        # Include cross_reference_engine for comprehensive research
        if len(detected_tools) >= 3:
            detected_tools.add("cross_reference_engine")

        return detected_tools

    def _infer_sources(self, tools: Set[str], query: str) -> Set[str]:
        """
        Infer which sources to search based on selected tools.

        Args:
            tools: Detected tools
            query: Original query

        Returns:
            Set of source types
        """
        sources: Set[str] = set()

        # Map tools to sources
        for tool in tools:
            if tool in self.TOOL_SOURCE_MAPPING:
                sources.update(self.TOOL_SOURCE_MAPPING[tool])

        # Check for explicit source keywords
        query_lower = query.lower()
        for source, patterns in self._compiled_source_patterns.items():
            for pattern in patterns:
                if pattern.search(query_lower):
                    sources.add(source)
                    break

        # Always include surface web as baseline
        sources.add("surface")

        return sources

    def _determine_privacy(self, query: str, tools: Set[str]) -> Tuple[str, bool]:
        """
        Determine privacy level and Tor activation.

        Args:
            query: Research query
            tools: Detected tools

        Returns:
            Tuple of (privacy_level, use_tor)
        """
        query_lower = query.lower()

        # Check for maximum privacy keywords
        for pattern in self._compiled_privacy_patterns.get("MAXIMUM", []):
            if pattern.search(query_lower):
                return "MAXIMUM", True

        # Check for high privacy keywords
        for pattern in self._compiled_privacy_patterns.get("HIGH", []):
            if pattern.search(query_lower):
                return "HIGH", False

        # Check tools that require high privacy
        high_privacy_tools = {"stealth_crawler", "leak_hunter", "threat_assessor"}
        if tools & high_privacy_tools:
            return "HIGH", "stealth_crawler" in tools

        return "STANDARD", False

    def _analyze_complexity(self, query: str) -> Tuple[float, bool]:
        """
        Analyze query complexity and determine ToT activation.

        Args:
            query: Research query

        Returns:
            Tuple of (complexity_score, use_tot)
        """
        # Use TotIntegrationLayer if available
        if self.tot_integration is not None:
            try:
                should_use, confidence = self.tot_integration.should_activate_tot(query)
                analysis = self.tot_integration.analyze_complexity(query)
                return analysis.score, should_use
            except Exception as e:
                logger.warning(f"ToT integration failed, using fallback: {e}")

        # Fallback: Simple complexity estimation
        return self._fallback_complexity_analysis(query)

    def _fallback_complexity_analysis(self, query: str) -> Tuple[float, bool]:
        """
        Fallback complexity analysis without ToT integration.

        Args:
            query: Research query

        Returns:
            Tuple of (complexity_score, use_tot)
        """
        query_lower = query.lower()
        words = query_lower.split()
        word_count = len(words)

        score = 0.0

        # Multi-step keywords (higher weight)
        multi_step = [
            "analyze", "compare", "evaluate", "assess", "explain",
            "determine", "investigate", "explore", "examine",
            "analyzovat", "porovnat", "zhodnotit", "vysvětlit",
        ]
        for keyword in multi_step:
            if keyword in query_lower:
                score += 0.25
                break

        # Question complexity
        question_count = query.count("?")
        score += min(0.3, question_count * 0.1)

        # Query length (more granular)
        if word_count > 50:
            score += 0.25
        elif word_count > 30:
            score += 0.2
        elif word_count > 15:
            score += 0.1

        # Alternatives/comparison (higher weight)
        alternatives = [
            "options", "pros and cons", "advantages", "alternatives",
            "compare", "versus", "vs", "trade-off", "možnosti", "výhody",
            "different approaches", "multiple strategies", "various methods",
        ]
        for keyword in alternatives:
            if keyword in query_lower:
                score += 0.2
                break

        # Multiple aspects/sub-questions indicators
        complexity_indicators = [
            "considering", "factors", "aspects", "dimensions",
            "trade-offs", "implications", "consequences", "impacts",
        ]
        for indicator in complexity_indicators:
            if indicator in query_lower:
                score += 0.15
                break

        # Multiple items to compare (comma-separated lists suggest complexity)
        comma_count = query.count(",")
        if comma_count >= 3:
            score += 0.15
        elif comma_count >= 2:
            score += 0.1

        # Cap at 1.0
        score = min(1.0, score)

        # ToT threshold
        use_tot = score >= 0.6

        return score, use_tot

    def _determine_tot_mode(self, complexity: float) -> str:
        """
        Determine ToT mode based on complexity.

        Args:
            complexity: Complexity score

        Returns:
            ToT mode string
        """
        if complexity >= 0.8:
            return "full"
        elif complexity >= 0.6:
            return "hybrid"
        return "standard"

    def _estimate_depth(self, tool_count: int, complexity: float) -> str:
        """
        Estimate research depth based on tools and complexity.

        Args:
            tool_count: Number of tools selected
            complexity: Complexity score

        Returns:
            Depth level string
        """
        # Check for explicit depth keywords
        # (This would be called with query in real implementation)

        # Score-based estimation
        depth_score = complexity + (tool_count * 0.1)

        if depth_score >= 1.0:
            return "EXHAUSTIVE"
        elif depth_score >= 0.7:
            return "DEEP"
        elif depth_score <= 0.3:
            return "QUICK"
        return "STANDARD"

    def _determine_models(self, profile: AutoResearchProfile) -> Set[str]:
        """
        Determine which models are needed based on profile.

        Args:
            profile: AutoResearchProfile

        Returns:
            Set of model names
        """
        models: Set[str] = set()

        # Map tools to models
        for tool in profile.tools:
            if tool in self.TOOL_MODEL_MAPPING:
                models.update(self.TOOL_MODEL_MAPPING[tool])

        # Always include hermes for general reasoning
        models.add("hermes")

        # Include modernbert for embedding/search tasks
        if len(profile.tools) > 2:
            models.add("modernbert")

        return models

    def _estimate_time(self, profile: AutoResearchProfile) -> float:
        """
        Estimate execution time based on profile.

        Args:
            profile: AutoResearchProfile

        Returns:
            Estimated time in seconds
        """
        base_time = 60.0  # Base 60 seconds

        # Tool multiplier
        tool_multiplier = 1.0 + (len(profile.tools) * 0.2)

        # Depth multiplier
        depth_multipliers = {
            "QUICK": 0.5,
            "STANDARD": 1.0,
            "DEEP": 2.0,
            "EXHAUSTIVE": 4.0,
        }
        depth_multiplier = depth_multipliers.get(profile.depth, 1.0)

        # ToT multiplier
        tot_multiplier = 1.5 if profile.use_tot else 1.0

        # Privacy multiplier (Tor is slower)
        privacy_multiplier = 1.3 if profile.use_tor else 1.0

        estimated_time = base_time * tool_multiplier * depth_multiplier * tot_multiplier * privacy_multiplier

        # Cap at reasonable limits
        return min(estimated_time, 1800.0)  # Max 30 minutes

    def _generate_reasoning(self, profile: AutoResearchProfile) -> str:
        """
        Generate human-readable reasoning for the profile.

        Args:
            profile: AutoResearchProfile

        Returns:
            Reasoning string
        """
        reasoning_parts = []

        # Tools reasoning
        if profile.tools:
            tool_list = ", ".join(sorted(profile.tools))
            reasoning_parts.append(f"Detected {len(profile.tools)} intelligence tools: {tool_list}")

        # Sources reasoning
        if profile.sources:
            source_list = ", ".join(sorted(profile.sources))
            reasoning_parts.append(f"Will search across {len(profile.sources)} source types: {source_list}")

        # Privacy reasoning
        if profile.privacy_level == "MAXIMUM":
            reasoning_parts.append("Maximum privacy required due to sensitive nature of query")
        elif profile.privacy_level == "HIGH":
            reasoning_parts.append("High privacy level selected for identity-related research")

        # Tor reasoning
        if profile.use_tor:
            reasoning_parts.append("Tor network enabled for anonymous access to dark web sources")

        # ToT reasoning
        if profile.use_tot:
            reasoning_parts.append(f"Tree of Thoughts ({profile.tot_mode} mode) activated for complex reasoning")

        # Depth reasoning
        if profile.depth == "EXHAUSTIVE":
            reasoning_parts.append("Exhaustive depth selected for comprehensive coverage")
        elif profile.depth == "DEEP":
            reasoning_parts.append("Deep analysis mode for thorough investigation")
        elif profile.depth == "QUICK":
            reasoning_parts.append("Quick scan mode for rapid overview")

        # Time reasoning
        reasoning_parts.append(f"Estimated execution time: {profile.max_time:.0f} seconds")

        return "; ".join(reasoning_parts)

    def get_capabilities(self) -> Dict[str, Any]:
        """Get analyzer capabilities."""
        return {
            "name": "autonomous_analyzer",
            "version": "1.0.0",
            "tools_supported": len(self.TOOL_KEYWORDS),
            "sources_supported": list(self.SOURCE_KEYWORDS.keys()),
            "privacy_levels": list(self.PRIVACY_KEYWORDS.keys()),
            "depth_levels": ["QUICK", "STANDARD", "DEEP", "EXHAUSTIVE"],
            "tot_modes": ["standard", "hybrid", "full"],
            "models": ["hermes", "modernbert", "gliner"],
            "languages": ["en", "cs"],
        }

    def health_check(self) -> bool:
        """Check if analyzer is operational."""
        try:
            # Test basic analysis
            test_profile = self.analyze("test query")
            return test_profile is not None and len(test_profile.tools) > 0
        except Exception as e:
            logger.error(f"Health check failed: {e}")
            return False


